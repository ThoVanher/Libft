#include "libft_bonus.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list *actual;

	actual = lst;
	if (!lst)
		return ;
	while (actual)
	{
		(*f)(actual->content);
		actual = actual->next;
	}
}
