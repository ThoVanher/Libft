#include "libft_bonus.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list *following;

	if (!lst)
		return ;
	while (*lst)
	{
		following = (*lst)->next;
		ft_lstdelone(*lst, del);
		*lst = following;
	}
}
