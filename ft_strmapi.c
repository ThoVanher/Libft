#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int				len;
	char			*new;
	unsigned int	i;

	len = ft_strlen((char *)s);
	i = 0;
	new = 0;
	if (s[i] == '\0')
		return (new);
	if (!(new = malloc((len + 1) * sizeof(char))))
		return (0);
	while (s[i])
	{
		new[i] = (*f)(i, s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
